#!/usr/bin/env node
/**
 * Sincroniza @forge/agent-contract → mirrors Deno (edge functions).
 * Rodar após alterar packages/agent-contract/src/{events,lifecycle}.ts
 *
 * Usage:
 *   npm run sync:agent-contract
 *   npm run check:agent-contract   (--check: falha se mirrors desatualizados)
 */
import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const root = resolve(process.cwd());
const checkOnly = process.argv.includes("--check");

function buildOutputs() {
  const eventsSrc = readFileSync(
    resolve(root, "packages/agent-contract/src/events.ts"),
    "utf8",
  );
  const lifecycleSrc = readFileSync(
    resolve(root, "packages/agent-contract/src/lifecycle.ts"),
    "utf8",
  );

  const eventsOut = `// AUTO-GENERATED by scripts/sync-agent-contract.mjs — do not edit.\n${eventsSrc}`;
  const lifecycleOut = `// AUTO-GENERATED by scripts/sync-agent-contract.mjs — do not edit.\n${lifecycleSrc.replace(
    'from "./events.ts"',
    'from "./agent-contract-events.ts"',
  )}`;

  return {
    [resolve(root, "supabase/functions/agent-run/_events.ts")]: eventsOut,
    [resolve(root, "supabase/functions/_shared/agent-contract-events.ts")]: eventsOut.replace(
      "@forge/agent-contract",
      "agent-contract-events",
    ),
    [resolve(root, "supabase/functions/_shared/agent-contract-lifecycle.ts")]: lifecycleOut,
  };
}

function readOrEmpty(path) {
  try {
    return readFileSync(path, "utf8");
  } catch {
    return null;
  }
}

const outputs = buildOutputs();

if (checkOnly) {
  const stale = [];
  for (const [path, expected] of Object.entries(outputs)) {
    const current = readOrEmpty(path);
    if (current !== expected) stale.push(path.replace(`${root}/`, ""));
  }
  if (stale.length) {
    console.error("FAIL: agent-contract mirrors out of sync:");
    for (const p of stale) console.error(`  - ${p}`);
    console.error("Run: npm run sync:agent-contract");
    process.exit(1);
  }
  console.log("OK: agent-contract mirrors in sync");
  process.exit(0);
}

for (const [path, content] of Object.entries(outputs)) {
  writeFileSync(path, content);
}
console.log("✓ agent-contract synced to edge mirrors");